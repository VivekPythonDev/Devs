str="python"
print(str[0])

# To retrive range of character we use : (Slicing Operator)

#1,2,3
print(str[1:4])

print(str[2:4])

# By default starting point is 0
print(str[:3])

# By default end point is 0
print(str[1:])

# By default no point
print(str[:])

# By string[start:stop:step]]
print(str[::1])
print(str[::2])

#Negative print..In reverse order
print(str[::-1])
